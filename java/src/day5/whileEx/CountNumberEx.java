package day5.whileEx;

public class CountNumberEx {

	public static void main(String[] args) {
		/* 1부터 10까지 출력하는 코드를 작성하세요.
		 * 반복횟수 : i는 1부터 i가 10까지//1이크면 크거나같다,10이크면 작거나같다// 1씩 증가
		 * 규칙성 : i를 출력
		 * */
		 int i = 1; //i는 1부터		 
		 while( i <= 10) { //i는 10까지 
			 System.out.println(i);
			 ++i; //i는 1씩 증가
			 	
		 }
	}
}
