package day3.ifEx;

public class ifEx1 {

	public static void main(String[] args) {
		//정수 num가 0이면 0입니다라고 출력하는 코드를 작성하세요.
		//조건문: 정수 num가 0이다
		//정수 num가 0과 같다
		int num = 10;
		if(num == 0) {
			//실행문: 0입니다라고 출력;
			System.out.println("0 is 0");
		}
		
		//정수 num가 0이 아니면 0이 아닙니다. 라고 출력하는 코드를 작성하세요.
		//num가 0이 아니다
		//num가 0과 같지 않다
		if(num != 0) {
			System.out.println(num + "is not 0");
		//if문이 또있으면, 각각 수행한다. 
			
		}
		
	}

}
